# Variable = A container for a value ( string, integer, boolean, float, etc. ) 
# A variable behaves as if it was the it contains

first_name = "John" # string
food = "Pizza" # string

print(f"Hello {first_name}, would you like some {food}?")

# f"..." is a Python formatted string literal.
# It means the string is an f-string.
# Inside the quotes, expressions in {} are evaluated and inserted into the string.